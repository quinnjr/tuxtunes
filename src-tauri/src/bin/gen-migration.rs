//! Generate a SQLite migration from the Prax schema.
//!
//! Run from `src-tauri/`:
//!
//! ```bash
//! cargo run --bin gen-migration -- prax/schema.prax prax/migrations/0001_initial
//! ```
//!
//! The Prax 0.7.3 CLI's `migrate dev` command hardcodes Postgres SQL regardless
//! of datasource. This helper drives `prax_migrate::sql::SqliteGenerator` directly
//! so we get SQLite-correct DDL. Remove when the upstream CLI gains SQLite support.

use std::path::PathBuf;
use std::process::ExitCode;

use prax_migrate::diff::SchemaDiffer;
use prax_migrate::sql::SqliteGenerator;
use prax_schema::validate_schema;

fn main() -> ExitCode {
    let args: Vec<String> = std::env::args().collect();
    if args.len() != 3 {
        eprintln!("usage: gen-migration <schema.prax> <output-dir>");
        return ExitCode::FAILURE;
    }

    let schema_path = PathBuf::from(&args[1]);
    let out_dir = PathBuf::from(&args[2]);

    let input = match std::fs::read_to_string(&schema_path) {
        Ok(s) => s,
        Err(e) => {
            eprintln!("error: read {}: {e}", schema_path.display());
            return ExitCode::FAILURE;
        }
    };

    let target = match validate_schema(&input) {
        Ok(s) => s,
        Err(e) => {
            eprintln!("error: schema validation: {e}");
            return ExitCode::FAILURE;
        }
    };

    let diff = match SchemaDiffer::new(target).diff() {
        Ok(d) => d,
        Err(e) => {
            eprintln!("error: diff: {e}");
            return ExitCode::FAILURE;
        }
    };

    let sql = SqliteGenerator.generate(&diff);

    if let Err(e) = std::fs::create_dir_all(&out_dir) {
        eprintln!("error: create {}: {e}", out_dir.display());
        return ExitCode::FAILURE;
    }

    let mut text = String::new();
    text.push_str("-- TuxTunes initial SQLite migration.\n");
    text.push_str("-- Generated by src-tauri/src/bin/gen-migration.rs.\n");
    text.push_str("-- Do not edit by hand; re-run the generator if the schema changes.\n\n");

    text.push_str(&sql.up);
    text.push('\n');

    if !sql.warnings.is_empty() {
        text.push_str("\n-- Generator warnings:\n");
        for w in &sql.warnings {
            text.push_str(&format!("-- {w}\n"));
        }
    }

    let out_file = out_dir.join("migration.sql");
    if let Err(e) = std::fs::write(&out_file, text) {
        eprintln!("error: write {}: {e}", out_file.display());
        return ExitCode::FAILURE;
    }

    println!("wrote {}", out_file.display());
    let stmt_count = sql.up.matches("CREATE TABLE").count() + sql.up.matches("CREATE INDEX").count();
    println!("  {} DDL statements", stmt_count);
    if !sql.warnings.is_empty() {
        println!("  {} warnings", sql.warnings.len());
    }
    ExitCode::SUCCESS
}
